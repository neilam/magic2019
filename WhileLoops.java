public class WhileLoops {
public static void main(String[] args) {
// while loops 

	int x= 0;
	while(x<5) {
		System.out.println("Hello, x=" + x);
		x++;
		
	}
	

	
// print our numbers 1-10;
	int number = 1; 
	while(number<11) {
		System.out.println(number);
		number++;
// number = number +1;
	} 
	for(int i=0; i<5; i++) {
		System.out.println("Current i=" + i);

}
}
}